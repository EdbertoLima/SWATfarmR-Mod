# Paste function pasting two character strings "_" as seperator
'%_%' <- function(a, b) paste(a, b, sep = "_")

# Paste function pasting two character strings "." as seperator
'%.%' <- function(a, b) paste(a, b, sep = ".")

# Paste function pasting two character strings "/" as seperator
'%//%' <- function(a, b) paste(a, b, sep = "/")
